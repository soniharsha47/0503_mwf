console.log(90>90 | 90==90)  // it need atleast one condition will true
console.log(90>90 || 90==90)  // it need atleast one condition will true

console.log(90>90 & 90==90)  // it need all conditions will true
console.log(90>90 && 90==90)  // it need all conditions will true

